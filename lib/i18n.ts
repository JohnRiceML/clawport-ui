export function localizeAgentDescription(
  agentId: string | null | undefined,
  description: string | null | undefined,
  locale: SupportedLocale,
): string {
  if (!description) return ''
  if (locale !== 'zh-CN') return description
  if (agentId && AGENT_DESCRIPTION_ZH_BY_ID[agentId]) {
    return AGENT_DESCRIPTION_ZH_BY_ID[agentId]
  }
  return AGENT_DESCRIPTION_ZH_BY_PHRASE[description] ?? description
}

export function localizeMemoryHealthCheck(
  check: { id: string; title: string; description: string },
  locale: SupportedLocale,
): { title: string; description: string } {
  if (locale !== 'zh-CN') return check

  const staticDescriptions: Partial<Record<string, string>> = {
    'vector-search-disabled':
      '当前智能体只能直接读取 MEMORY.md。建议在 openclaw.json 中启用 vector search，这样才能对全部记忆文件做语义检索。',
    'unindexed-vector':
      '配置里已经开启 memory search，但当前还没有索引。完成建索引之前，智能体无法使用语义检索。',
    'stale-evergreen':
      '长期记忆文件如果长时间不更新，里面可能会保留过时事实。建议定期复查，确保信息仍然有效。',
    'no-config':
      '当前使用的是 OpenClaw 默认配置。若在 openclaw.json 里显式配置 memorySearch，就可以调整检索权重、时间衰减和缓存策略。',
    'decay-disabled':
      '如果不启用 temporal decay，旧的 daily logs 在检索时会和近期内容获得同等权重。开启后可以优先命中最近的上下文。',
  }

  if (staticDescriptions[check.id]) {
    return { title: check.title, description: staticDescriptions[check.id]! }
  }

  const description = check.description
  if (check.id === 'memory-md-lines') {
    const match = description.match(/MEMORY\.md has (\d+) lines\./)
    if (match) {
      const lineCount = Number(match[1])
      return {
        title: check.title,
        description:
          check.title.includes('approaching')
            ? `MEMORY.md 当前有 ${lineCount} 行。最佳实践是尽量控制在 200 行以内，建议把次要内容拆到独立主题文件中。`
            : `MEMORY.md 当前有 ${lineCount} 行。超过 200 行后的内容在加载到智能体上下文时会被截断，建议拆分为主题文件并在 MEMORY.md 中保留链接。`,
      }
    }
  }

  if (check.id === 'file-size') {
    return {
      title: check.title,
      description: check.title.includes('100KB')
        ? '记忆文件过大会稀释检索质量。Chunk 检索更适合结构清晰、聚焦单一主题且小于 50KB 的文件。'
        : '这些文件已经接近 100KB 阈值，建议提前拆分，以提升检索精度。',
    }
  }

  if (check.id === 'stale-daily-logs') {
    const staleInfo = description.match(/(\d+) log(?:s are| is) over 60 days old/)
    if (staleInfo) {
      const count = Number(staleInfo[1])
      return {
        title: check.title,
        description: `${count} 份日志已经超过 60 天。过旧的 daily logs 会给检索结果引入噪声，也会拖慢检索效率。`,
      }
    }
    return {
      title: check.title,
      description: '建议回看这些较旧的 daily logs，把有价值的模式沉淀到 evergreen 文件，其余内容可以归档或删除。',
    }
  }

  if (check.id === 'total-size') {
    const mb = description.match(/Total memory size is ([\d.]+)MB\./)
    if (mb) {
      return {
        title: check.title,
        description: `当前记忆总量约 ${mb[1]}MB。记忆仓过大会降低检索质量，并增加 embedding 成本。`,
      }
    }
    const kb = description.match(/Total memory size is (\d+)KB\./)
    if (kb) {
      return {
        title: check.title,
        description: `当前记忆总量约 ${kb[1]}KB。建议适度清理，保持检索足够快且内容相关。`,
      }
    }
  }

  if (check.id === 'stale-index') {
    const match = description.match(/(\d+) file(?:s were| was) modified after the last index \((\d+)h ago\)\./)
    if (match) {
      return {
        title: check.title,
        description: `上次建索引后已有 ${match[1]} 个文件发生变更，距今约 ${match[2]} 小时。智能体可能看不到这些最新修改。`,
      }
    }
  }

  return { title: check.title, description }
}

